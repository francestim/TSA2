document.getElementById('mon').addEventListener('click', function() {
    let box = document.getElementById('box-ani');
    box.style.backgroundColor = 'white';
    box.style.border = '10px solid purple';
    box.style.display= 'block';
    setTimeout(function() {
      box.style.transform = 'translateY(250px)';
    }, 100);
  });

